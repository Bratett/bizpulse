# ---------------------------------------------------------------------------


def render_vat_report_pdf(report: dict) -> bytes:
    """Render a GRA VAT Return as a PDF.

    Parameters
    ----------
    report:
        Dictionary matching the shape of
        :class:`models.VATReportResponse`.

    Returns
    -------
    bytes
        PDF file content.
    """
    business_name = _esc(report.get("business_name", ""))
    business_tin = _esc(report.get("business_tin", "N/A"))
    period_start = _format_date(report.get("period_start"))
    period_end = _format_date(report.get("period_end"))
    period_id = _esc(report.get("period_id", ""))
    computation_date = _format_date(report.get("computation_date"))

    taxable_sales = report.get("taxable_sales_pesewas", 0)
    output_vat = report.get("output_vat_pesewas", 0)
    taxable_purchases = report.get("taxable_purchases_pesewas", 0)
    input_vat = report.get("input_vat_pesewas", 0)
    net_vat = report.get("net_vat_payable_pesewas", 0)
    nhil = report.get("nhil_pesewas", 0)
    getfund = report.get("getfund_pesewas", 0)
    total_payable = report.get("total_payable_pesewas", 0)

    html_content = f"""\
<!DOCTYPE html>
<html lang="en">
<head><meta charset="utf-8"><style>{_BASE_CSS}</style></head>
<body>
<h1>VAT / NHIL / GETFund Return</h1>

<div class="header-block">
    <table class="meta-table">
        <tr><td class="label">Business Name:</td><td>{business_name}</td></tr>
        <tr><td class="label">TIN:</td><td>{business_tin}</td></tr>
        <tr><td class="label">Period:</td><td>{period_start} &ndash; {period_end}</td></tr>
        <tr><td class="label">Period ID:</td><td>{period_id}</td></tr>
        <tr><td class="label">Computation Date:</td><td>{computation_date}</td></tr>
    </table>
</div>

<h2>Output Tax (Sales)</h2>
<table>
    <thead>
        <tr><th>Description</th><th style="width:35%">Amount</th></tr>
    </thead>
    <tbody>
        <tr>
            <td>Total Taxable Sales</td>
            <td class="amount">{format_pesewas(taxable_sales)}</td>
        </tr>
        <tr>
            <td>Output VAT</td>
            <td class="amount">{format_pesewas(output_vat)}</td>
        </tr>
    </tbody>
</table>

<h2>Input Tax (Purchases)</h2>
<table>
    <thead>
        <tr><th>Description</th><th style="width:35%">Amount</th></tr>
    </thead>
    <tbody>
        <tr>
            <td>Total Taxable Purchases</td>
            <td class="amount">{format_pesewas(taxable_purchases)}</td>
        </tr>
        <tr>
            <td>Input VAT</td>
            <td class="amount">{format_pesewas(input_vat)}</td>
        </tr>
    </tbody>
</table>

<h2>Tax Payable Summary</h2>
<table>
    <thead>
        <tr><th>Component</th><th style="width:35%">Amount</th></tr>
    </thead>
    <tbody>
        <tr>
            <td>Net VAT Payable (Output VAT &minus; Input VAT)</td>
            <td class="amount">{format_pesewas(net_vat)}</td>
        </tr>
        <tr>
            <td>NHIL (National Health Insurance Levy)</td>
            <td class="amount">{format_pesewas(nhil)}</td>
        </tr>
        <tr>
            <td>GETFund Levy</td>
            <td class="amount">{format_pesewas(getfund)}</td>
        </tr>
        <tr class="total">
            <td>Total Amount Payable</td>
            <td class="amount">{format_pesewas(total_payable)}</td>
        </tr>
    </tbody>
</table>

<div class="note">
    <p><strong>Note:</strong> Input VAT is estimated based on standard-rated
    expense transactions. Actual input VAT may differ.</p>
    <p>{_esc(_GENERAL_NOTE)}</p>
</div>

{_FOOTER_HTML}
</body>
</html>
"""
    pdf_doc = weasyprint.HTML(string=html_content, url_fetcher=_SAFE_URL_FETCHER)
    return pdf_doc.write_pdf()


