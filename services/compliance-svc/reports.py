# ---------------------------------------------------------------------------
# Invoice
# ---------------------------------------------------------------------------


def render_invoice_pdf(
    business: dict,
    request_data: dict,
    vat_rate_bps: int,
) -> bytes:
    """Render a tax invoice as a PDF.

    Parameters
    ----------
    business:
        Business details with keys ``legal_name``, ``trading_name``,
        ``tax_identification_number``, ``country_code``.
    request_data:
        Invoice data with keys ``customer_name``, ``customer_tin``
        (optional), ``line_items`` (list of dicts with ``description``,
        ``quantity``, ``unit_price_pesewas``, ``vat_applicable``),
        and ``notes`` (optional).
    vat_rate_bps:
        VAT rate in basis points (e.g. 1500 = 15.00%).

    Returns
    -------
    bytes
        PDF file content.
    """
    legal_name = _esc(business.get("legal_name", ""))
    trading_name = _esc(business.get("trading_name", ""))
    seller_tin = _esc(business.get("tax_identification_number", "N/A"))
    country_code = _esc(business.get("country_code", "GH"))

    customer_name = _esc(request_data.get("customer_name", ""))
    customer_tin = _esc(request_data.get("customer_tin") or "")
    notes = _esc(request_data.get("notes") or "")
    line_items = request_data.get("line_items", [])

    vat_rate_pct = vat_rate_bps / 100  # e.g. 15.00
    invoice_date = datetime.utcnow().strftime("%d %b %Y")

    # Build line-item rows and running totals
    subtotal_pesewas = 0
    total_vat_pesewas = 0
    rows_html = ""

    for idx, item in enumerate(line_items, start=1):
        desc = _esc(item.get("description", ""))
        qty = item.get("quantity", 1.0)
        unit_price = item.get("unit_price_pesewas", 0)
        vat_applicable = item.get("vat_applicable", True)

        line_subtotal = int(qty * unit_price)
        if vat_applicable:
            line_vat = int(round(line_subtotal * vat_rate_bps / 10_000))
        else:
            line_vat = 0
        line_total = line_subtotal + line_vat

        subtotal_pesewas += line_subtotal
        total_vat_pesewas += line_vat

        vat_label = format_pesewas(line_vat) if vat_applicable else "Exempt"

        rows_html += f"""\
        <tr>
            <td style="text-align:center">{idx}</td>
            <td>{desc}</td>
            <td style="text-align:center">{qty:g}</td>
            <td class="amount">{format_pesewas(unit_price)}</td>
            <td class="amount">{format_pesewas(line_subtotal)}</td>
            <td class="amount">{vat_label}</td>
            <td class="amount">{format_pesewas(line_total)}</td>
        </tr>
"""

    grand_total_pesewas = subtotal_pesewas + total_vat_pesewas

    customer_tin_row = ""
    if customer_tin:
        customer_tin_row = (
            f'<tr><td class="label">Customer TIN:</td>'
            f"<td>{customer_tin}</td></tr>"
        )

    notes_section = ""
    if notes:
        notes_section = f"""\
<h3>Notes</h3>
<p style="font-size:10pt;color:#555">{notes}</p>
"""

    display_name = trading_name if trading_name else legal_name

    html_content = f"""\
<!DOCTYPE html>
<html lang="en">
<head><meta charset="utf-8"><style>{_BASE_CSS}
.invoice-header {{
    display: flex;
    justify-content: space-between;
    margin-bottom: 20px;
}}
.invoice-header .seller {{
    flex: 1;
}}
.invoice-header .title {{
    text-align: right;
    flex: 1;
}}
.invoice-header .title h1 {{
    border-bottom: none;
    margin-bottom: 4px;
}}
</style></head>
<body>

<table style="width:100%;border:none;margin-bottom:20px">
    <tr style="border:none">
        <td style="border:none;vertical-align:top;width:60%">
            <h2 style="margin-bottom:4px">{display_name}</h2>
            <p style="font-size:10pt;margin:2px 0;color:#555">{legal_name}</p>
            <p style="font-size:10pt;margin:2px 0">TIN: {seller_tin}</p>
            <p style="font-size:10pt;margin:2px 0">Country: {country_code}</p>
        </td>
        <td style="border:none;vertical-align:top;text-align:right">
            <h1 style="border-bottom:none;text-align:right">TAX INVOICE</h1>
            <p style="font-size:10pt;margin:2px 0">Date: {invoice_date}</p>
        </td>
    </tr>
</table>

<div class="header-block">
    <h3>Bill To</h3>
    <table class="meta-table">
        <tr><td class="label">Customer:</td><td>{customer_name}</td></tr>
        {customer_tin_row}
    </table>
</div>

<h2>Line Items</h2>
<table>
    <thead>
        <tr>
            <th style="width:5%;text-align:center">#</th>
            <th>Description</th>
            <th style="width:8%;text-align:center">Qty</th>
            <th style="width:14%">Unit Price</th>
            <th style="width:14%">Subtotal</th>
            <th style="width:14%">VAT ({vat_rate_pct:g}%)</th>
            <th style="width:14%">Total</th>
        </tr>
    </thead>
    <tbody>
{rows_html}\
    </tbody>
</table>

<table style="width:50%;margin-left:auto;margin-bottom:20px">
    <tbody>
        <tr>
            <td style="font-weight:600;border:none;text-align:right;padding-right:16px">Subtotal:</td>
            <td class="amount" style="border:none">{format_pesewas(subtotal_pesewas)}</td>
        </tr>
        <tr>
            <td style="font-weight:600;border:none;text-align:right;padding-right:16px">VAT ({vat_rate_pct:g}%):</td>
            <td class="amount" style="border:none">{format_pesewas(total_vat_pesewas)}</td>
        </tr>
        <tr class="total">
            <td style="font-weight:700;border:none;border-top:2px solid #1B4332;text-align:right;padding-right:16px">Grand Total:</td>
            <td class="amount" style="border:none;border-top:2px solid #1B4332">{format_pesewas(grand_total_pesewas)}</td>
        </tr>
    </tbody>
</table>

{notes_section}

<div class="note">
    <p>{_esc(_GENERAL_NOTE)}</p>
</div>

{_FOOTER_HTML}
</body>
</html>
"""
    pdf_doc = weasyprint.HTML(string=html_content, url_fetcher=_SAFE_URL_FETCHER)
    return pdf_doc.write_pdf()
