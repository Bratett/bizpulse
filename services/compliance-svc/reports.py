"""
GRA-formatted tax report generation for BizPulse AI compliance-svc.

Renders VAT returns, CIT returns, and tax invoices as HTML and converts
to PDF bytes via weasyprint.  All monetary values arrive in pesewas
(minor units) and are formatted to Ghana Cedis (GHS) for display.

SSRF mitigation (MF-4): every weasyprint call uses a url_fetcher that
returns an empty payload so external resources can never be loaded.
"""

from __future__ import annotations

import html
from datetime import datetime
from typing import Any

try:
    import weasyprint
except ImportError:
    weasyprint = None  # PDF generation unavailable without system deps

# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

_SAFE_URL_FETCHER = lambda url, **kwargs: {  # noqa: E731
    "string": b"",
    "mime_type": "text/plain",
}
"""Block all external resource loads to prevent SSRF (MF-4)."""


def format_pesewas(amount: int) -> str:
    """Convert pesewas integer to display string ``GHS 1,234.56``.

    Negative amounts are rendered as ``-GHS 1,234.56``.
    """
    if amount < 0:
        return f"-GHS {abs(amount) / 100:,.2f}"
    return f"GHS {amount / 100:,.2f}"


def _esc(value: Any) -> str:
    """HTML-escape a value for safe template interpolation."""
    if value is None:
        return ""
    return html.escape(str(value))


def _format_date(d: Any) -> str:
    """Return a human-friendly date string from date/datetime/str."""
    if d is None:
        return ""
    if isinstance(d, datetime):
        return d.strftime("%d %b %Y")
    # date or str
    try:
        return d.strftime("%d %b %Y")
    except AttributeError:
        return _esc(str(d))


